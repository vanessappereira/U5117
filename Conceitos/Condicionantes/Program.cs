namespace Condicionantes
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int n1 = 20, n2 = 18;

            if (n1 > n2)
            {
                Console.WriteLine($"{n1} é maior que {n2}");
            }
            else
            {
                Console.WriteLine($"{n1} é menor que {n2}");
            }
        }
    }
}
